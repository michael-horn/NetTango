package tween;


public class Point3D {

   public float x = 0;

   public float y = 0;

   public float z = 0;

   public Point3D() {
      this.x = 0;
      this.y = 0;
      this.z = 0;
   }

   public Point3D(float x, float y, float z) {
      this.x = x;
      this.y = y;
      this.z = z;
   }

}

